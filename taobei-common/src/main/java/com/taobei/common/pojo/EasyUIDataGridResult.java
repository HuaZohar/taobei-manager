package com.taobei.common.pojo;

import java.util.List;

public class EasyUIDataGridResult {

	private long total;
	private List<?> rows; //?  表示类型不确定    与T有区别  注意
	
	public long getTotal() {
		return total;
	}
	public void setTotal(long total) {
		this.total = total;
	}
	public List<?> getRows() {
		return rows;
	}
	public void setRows(List<?> rows) {
		this.rows = rows;
	}
	
}
